const { Schema, model } = require("mongoose");

const CoffeeSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  category: {
    type: String,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
});

const Coffee = model("Coffee", CoffeeSchema);

module.exports = Coffee;
